package WAP;

public class IntegerMultiply {
    public static void main(String[] args) {
       int a = 10;
       int b = 20;
       int c = 30;
       int d = 50;
       int sum = a + b;
       int sub = d - a;
       System.out.println(a * c);
       System.out.println(a * d);
       System.out.println(b * d);
       System.out.println(sum);
       System.out.println(sub);
}
}